    def open_obj_dialog(self):
        path, _ = QFileDialog.getOpenFileName(self, "Open artery OBJ", str(PROJECT_ROOT), "OBJ Files (*.obj);;All Files (*)")
        if path:
            self.load_obj(Path(path))

    def load_obj(self, path: Path):
        try:
            self.mesh = parse_obj(path)
            self.report = load_branch_report(path)
            self.summary = self.load_pipeline_summary(path)
            self.obj_path = path
            self.lbl_path.setText(str(path))
            self.populate_table()
            self.populate_validation_panel()
            self.update_angle_controls_from_summary()
            self.render_mesh()
        except Exception as exc:
            QMessageBox.critical(self, "Failed to load OBJ", str(exc))

    def populate_table(self):
        self.table.setRowCount(0)
        if not self.mesh:
            return
        branch_ids = sorted(
            branch_id
            for branch_id in {branch_id_from_name(part.name) for part in self.mesh.parts}
            if branch_id is not None
        )
        for row_idx, branch_id in enumerate(branch_ids):
            row = self.report.get(branch_id, {})
            status = row.get("status", self.status_from_parts(branch_id))
            values = [
                str(branch_id),
                status,
                format_number(row.get("estimated_diameter_stenosis_pct"), 1),
                format_number(row.get("min_radius_mm"), 3),
                format_number(row.get("ref_radius_mm_p80"), 3),
                format_number(row.get("median_residual_mm"), 2),
                format_number(row.get("p90_residual_mm"), 2),
            ]
            self.table.insertRow(row_idx)
            for col_idx, value in enumerate(values):
                item = QTableWidgetItem(value)
                if col_idx == 0:
                    item.setData(Qt.UserRole, branch_id)
                self.table.setItem(row_idx, col_idx, item)

    def status_from_parts(self, branch_id: int) -> str:
        if not self.mesh:
            return ""
        for part in self.mesh.parts:
            if branch_id_from_name(part.name) == branch_id:
                return part.material
        return ""

    def load_pipeline_summary(self, obj_path: Path) -> Dict[str, object]:
        candidates = [
            obj_path.with_name("pipeline_summary.json"),
            obj_path.with_name("hybrid_qca_radius_summary.json"),
            obj_path.with_name("filtered_epipolar_summary.json"),
            obj_path.with_name("smoothed_junction_summary.json"),
        ]
        for path in candidates:
            if path.exists():
                try:
                    import json

                    with open(path, "r", encoding="utf-8") as f:
                        return json.load(f)
                except Exception:
                    continue
        return {}

    def populate_validation_panel(self):
        self.lbl_summary.setText(self.format_summary_text())
        self.combo_overlay.blockSignals(True)
        self.combo_overlay.clear()
        for label, path in self.find_validation_images():
            self.combo_overlay.addItem(label, str(path))
        self.combo_overlay.blockSignals(False)
        self.update_overlay_image()

    def format_summary_text(self) -> str:
        if not self.summary:
            return "No summary JSON found beside this OBJ."
        lines = []
        if "branch_status_counts" in self.summary:
            counts = self.summary.get("branch_status_counts", {})
            lines.append(
                "Branches: "
                f"reliable {counts.get('reliable', 0)} | "
                f"usable {counts.get('usable', 0)} | "
                f"single-view {counts.get('single_view_preserved', 0)}"
            )
        if "num_xa_clips_found" in self.summary:
            lines.append(f"XA clips found: {self.summary.get('num_xa_clips_found')}")
        for key in ("view_a", "view_b"):
            view = self.summary.get(key)
            if isinstance(view, dict):
                lines.append(
                    f"{key}: clip {view.get('clip_index', '?')} frame {view.get('frame', '?')} "
                    f"P {view.get('primary_angle_deg', '?')} S {view.get('secondary_angle_deg', '?')}"
                )
        if "warning" in self.summary:
            lines.append(str(self.summary["warning"]))
        if not lines:
            lines.append("Summary loaded.")
        return "\n".join(lines)

