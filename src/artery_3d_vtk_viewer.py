    def update_angle_controls_from_summary(self):
        view = self.summary.get("view_a") if isinstance(self.summary, dict) else None
        if not isinstance(view, dict):
            return
        try:
            self.spin_primary.setValue(float(view.get("primary_angle_deg", 0.0)))
            self.spin_secondary.setValue(float(view.get("secondary_angle_deg", 0.0)))
        except Exception:
            return

    @staticmethod
    def rotation_from_angles(primary_deg: float, secondary_deg: float):
        if np is None:
            raise RuntimeError("NumPy is required for angle-based camera views.")

        primary = math.radians(primary_deg)
        secondary = math.radians(secondary_deg)
        cp, sp = math.cos(primary), math.sin(primary)
        cs, ss = math.cos(secondary), math.sin(secondary)

        rotate_primary = np.array(
            [
                [cp, 0.0, sp],
                [0.0, 1.0, 0.0],
                [-sp, 0.0, cp],
            ],
            dtype=float,
        )
        rotate_secondary = np.array(
            [
                [1.0, 0.0, 0.0],
                [0.0, cs, -ss],
                [0.0, ss, cs],
            ],
            dtype=float,
        )
        return rotate_primary @ rotate_secondary

    def set_camera_from_angles(self, primary_deg: float, secondary_deg: float):
        if not self.all_actors:
            return
        if np is None:
            QMessageBox.warning(self, "Missing Dependency", "NumPy is required for angle-based camera views.")
            return

        rotation = self.rotation_from_angles(primary_deg, secondary_deg)
        source_direction = rotation @ np.array([0.0, 0.0, -1.0], dtype=float)
        detector_up = rotation @ np.array([0.0, 1.0, 0.0], dtype=float)
        source_direction, detector_up = self.apply_image_orientation(source_direction, detector_up)
        self.set_absolute_camera(source_direction, detector_up, scale_factor=0.62, distance_factor=3.0)

    def set_summary_angle_view(self, key: str):
        view = self.summary.get(key) if isinstance(self.summary, dict) else None
        if not isinstance(view, dict):
            QMessageBox.information(self, "No DICOM Angle", f"No {key} angle found beside this OBJ.")
            return
        try:
            primary = float(view.get("primary_angle_deg", 0.0))
            secondary = float(view.get("secondary_angle_deg", 0.0))
        except Exception:
            QMessageBox.warning(self, "Invalid DICOM Angle", f"The {key} angle values are not numeric.")
            return
        self.spin_primary.setValue(primary)
        self.spin_secondary.setValue(secondary)
        self.set_camera_from_angles(primary, secondary)

    def apply_manual_angle_view(self):
        self.set_camera_from_angles(self.spin_primary.value(), self.spin_secondary.value())

    def highlight_selected_branch(self):
        selected_items = self.table.selectedItems()
        selected_branch = None
        if selected_items:
            selected_branch = self.table.item(selected_items[0].row(), 0).data(Qt.UserRole)

        for branch_id, actors in self.branch_actors.items():
            for actor in actors:
                base_status = self.status_from_parts(branch_id)
                base_color = MATERIAL_COLORS.get(base_status, (0.78, 0.78, 0.78))
                if selected_branch is not None and branch_id == selected_branch:
                    actor.GetProperty().SetColor(1.0, 0.05, 0.05)
                    actor.GetProperty().SetSpecular(0.5)
                    actor.GetProperty().SetOpacity(1.0)
                else:
                    actor.GetProperty().SetColor(*base_color)
                    actor.GetProperty().SetSpecular(0.28)
                    actor.GetProperty().SetOpacity(1.0)
        self.vtk_widget.GetRenderWindow().Render()


